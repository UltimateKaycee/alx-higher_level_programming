def divisible_by_2(my_list=[]):
    """To find all the multiples of 2 in a given list."""
    all_multiples = []
    for start in range(len(my_list)):
        if my_list[tart] % 2 == 0:
            all_multiples.append(True)
        else:
            all_multiples.append(False)

    return (all_multiples)
